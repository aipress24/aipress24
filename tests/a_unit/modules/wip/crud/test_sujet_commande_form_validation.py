"""Unit tests for `SujetForm` + `CommandeForm` validation in
`app.modules.wip.crud.cbvs._forms`.

`SujetForm` is what a journalist fills in to *pitch* a story idea to
a target media. `CommandeForm` is the symmetric form for a media
*commissioning* a story from a freelancer. Both share the same
taxonomy fields (genre / section / topic / sector / media_id) and a
country selector, but differ in their date workflow :

* `SujetForm` : `date_limite_validite` (offer expiry) +
  `date_parution_prevue` (planned publication).
* `CommandeForm` : adds `date_bouclage` (copy-deadline) and
  `date_paiement` (when the commissioner will pay).

WTForms validation runs in-process — no DB, no HTTP — so this file
lands cleanly in a_unit. The forms are auto-generated (see the
"Autogenerated. DO NOT EDIT." banner in the source) ; pinning the
validators here catches three classes of silent regression :

1. **Loosening** : an InputRequired silently dropped from one of the
   four taxonomy selectors, `media_id`, or any of the dates — letting
   under-specified pitches / commandes reach the workflow queues.
2. **Tightening on titre/contenu** : both forms intentionally leave
   `titre` + `contenu` *without* InputRequired (draft-friendly), so
   the journalist can save a stub before fleshing it out. A future
   « add InputRequired to titre » would silently block legitimate
   drafts ; pin the contract here.
3. **Shape drift** : a field renamed without updating `Meta.groups`,
   which the rendering layer iterates ; the template would silently
   drop the field from the rendered page. The two forms also differ
   in their date set : pin each so a CommandeForm date silently
   appearing in SujetForm (or vice-versa) is caught.

Why no mocks/patches : the forms are pure declarative WTForms
classes. We exercise them by constructing a real `Form` instance
against an `ImmutableMultiDict` payload (exactly what Flask does
behind the scenes) and asserting on `form.errors` / `form.data` —
tangible state outputs, not internal interactions.

`RichSelectField` / `SimpleRichSelectField` derive from `SelectField`
and pull their choice list from the taxonomy cache (DB-backed).
That cache is not seeded in a_unit fixtures, so we set each select
field's `choices` to a stable single-entry list before validating.
The production view wires real choices from a DB query before
calling `form.validate()` — our stub mirrors the same contract
without booting the taxonomy.
"""

from __future__ import annotations

from typing import TYPE_CHECKING

import pytest
from werkzeug.datastructures import ImmutableMultiDict

from app.modules.wip.crud.cbvs._forms import CommandeForm, SujetForm

if TYPE_CHECKING:
    from flask import Flask


# Stub vocabulary values. They are intentionally non-semantic — we
# only need them to round-trip through SelectField pre-validation.
_STUB_GENRE = "STUB-GENRE"
_STUB_SECTION = "STUB-SECTION"
_STUB_TOPIC = "STUB-TOPIC"
_STUB_SECTOR = "STUB-SECTOR"
_STUB_MEDIA_ID = "42"

_GOOD_TITRE = "Un sujet de reportage"
_GOOD_CONTENU = "Brief du sujet à proposer."
_GOOD_DATETIME = "2025-03-15T10:30"


_SELECT_FIELDS: tuple[tuple[str, str], ...] = (
    ("genre", _STUB_GENRE),
    ("section", _STUB_SECTION),
    ("topic", _STUB_TOPIC),
    ("sector", _STUB_SECTOR),
    ("media_id", _STUB_MEDIA_ID),
)


def _stub_choices(form) -> None:
    """Populate each SelectField's `choices` with a single stable
    entry. The production view does this from a DB query right before
    validation ; we mirror the contract without a seeded taxonomy."""
    for name, good in _SELECT_FIELDS:
        form[name].choices = [(good, good)]


