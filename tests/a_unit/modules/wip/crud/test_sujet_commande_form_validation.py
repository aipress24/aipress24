_SUJET_DATE_FIELDS = ["date_limite_validite", "date_parution_prevue"]

_SUJET_REQUIRED_FIELDS = [
    "genre",
    "section",
    "topic",
    "sector",
    "media_id",
    "date_limite_validite",
    "date_parution_prevue",
]


def _sujet_baseline() -> dict[str, str]:
    """Smallest dict that validates against a stubbed-choice form."""
    return {
        "titre": _GOOD_TITRE,
        "contenu": _GOOD_CONTENU,
        "genre": _STUB_GENRE,
        "section": _STUB_SECTION,
        "topic": _STUB_TOPIC,
        "sector": _STUB_SECTOR,
        "media_id": _STUB_MEDIA_ID,
        "date_limite_validite": _GOOD_DATETIME,
        "date_parution_prevue": _GOOD_DATETIME,
    }


def _make_sujet_form(app: Flask, data: dict | None = None) -> SujetForm:
    payload = ImmutableMultiDict(data or {})
    with app.test_request_context():
        form = SujetForm(payload)
        _stub_choices(form)
        return form


class TestSujetFormRequiredFields:
    """Pin every InputRequired the source declares. A future refactor
    that drops one of these would silently let half-built sujets ship
    to the journalist's outbox."""

    def test_baseline_validates(self, app: Flask):
        """Sanity : the minimal-good payload validates. Anchors the
        rest of the suite — if this breaks, every other test is
        suspect."""
        form = _make_sujet_form(app, _sujet_baseline())
        assert form.validate(), f"Baseline should validate, errors={form.errors}"

    @pytest.mark.parametrize("field_name", _SUJET_REQUIRED_FIELDS)
    def test_required_field_blank_fails(self, app: Flask, field_name: str):
        """Each declared-required field, when blanked, must surface
        an error on *that* field."""
        payload = _sujet_baseline()
        payload[field_name] = ""
        form = _make_sujet_form(app, payload)
        assert not form.validate()
        assert form[field_name].errors, (
            f"Expected error on {field_name}, got errors={form.errors}"
        )

    @pytest.mark.parametrize("field_name", _SUJET_REQUIRED_FIELDS)
    def test_required_field_missing_fails(self, app: Flask, field_name: str):
        """Same as blank, but the key isn't even present in the POST.
        WTForms treats missing-key and blank-value identically for
        InputRequired ; pin that contract."""
        payload = _sujet_baseline()
        payload.pop(field_name)
        form = _make_sujet_form(app, payload)
        assert not form.validate()
        assert form[field_name].errors


